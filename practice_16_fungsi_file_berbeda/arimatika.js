// function lingkaran
function lingkaran(r) {
    return 3.14 * r * r;
}

// function penjumlahan
function penjumlahan(a, b) {
    return a + b;
}

function penjumlahanTiga(a, b, c) {
    return a + b +c;
    
}

// function perkalian
function perkalian(a, b) {
    return a * b;
}

function perkalianTiga(a, b, c) {
    return a * b * c;
}
    