
// deklarasi string menggunakan single quote (') dan double quote ("")
let var_string = "Hello World";
// typeof untuk mengetahui tipe data
console.log(typeof(var_string));
console.log(var_string[6]);

let array_string = ["Hello", "World", "!"];
console.log(array_string[1][2]);

// deklarasi number
let var_number = 10;
console.log(typeof(var_number));

// deklarasi boolean tru dan false
let var_boolean = false;
console.log(typeof(var_boolean));

// deklarasi null
let var_null = null
console.log(typeof(var_null))

// deklarasi undefined
let var_undefined = undefined;
console.log(typeof(var_undefined));

// deklarasi tipe data array #1
let var_array = ["Hello World", 2, 3 , 4, 5];
console.log(var_array);

// deklarasi tipe data array #1
let var_array_new = new Array(1, 2, "Hello World", 4, 5);
console.log(var_array_new);

// deklarasi object #1
let var_object = { }
console.log(var_object);

// deklarasi object #2
let var_object_new = new Object();
var_object_new.first = "John";
var_object_new.last = "Doe"; // cara pemanggilan object #1
var_object_new["ttl"] = "indonesia, 17 agustus 1945"; // cara pemanggilan object #2
console.log(var_object_new);

